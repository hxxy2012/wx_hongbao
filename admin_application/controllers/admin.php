	function index(){
		$list = $this->M_common->querylist("SELECT name,status,url from {$this->table_}common_admin_nav where pid = 0 and status = 1   order by disorder,id asc ");	
		$sql_login_log = "SELECT * FROM {$this->table_}common_adminloginlog WHERE username = '{$this->username}' order by id desc limit 2 " ;
		$login_info = $this->M_common->querylist($sql_login_log);
//		print_r($list);
		for($i=0;$i<count($list);$i++){
			if($list[$i]["url"]!=""){
				$arr = explode("/",$list[$i]["url"]);
				$isshow = $this->permition_for($arr[0],$arr[1]);//控制器 方法
			}
			else{
				$isshow = false;
			}
			$list[$i]["isshow"]=$isshow;
		}		
		$data = array(
			'list'=>$list,
			'username'=>$this->username,
			'group_name'=>"<font color='red'>".$this->group_name."</font>",
			'login_info'=>$login_info							
		);		
		$this->load->view(__TEMPLET_FOLDER__."/views_index",$data);
	}
	
