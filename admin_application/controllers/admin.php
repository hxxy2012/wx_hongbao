	private function doAddClass($item,$pid=45135,$typeid = 11,$typename='基础资料') {
		
		$status = '1';//开启
		if (is_array($item)) {//如果插入分类为数组时,多个分类
			$result = array();//返回插入的id数组
			foreach ($item as $key => $value) {
				$model = array('name' => $value,
						'pid' => $pid,
						'typeid' => $typeid,
						'typename' => $typename,
						'status' => $status
					);
				$data = $this->cd->Insert($model);//进行插入
				$result[] = $data['insert_id']?$data['insert_id']:'';
			}
			return $result;
		} else {//单个分类
			$model = array('name' => $item,
						'pid' => $pid,
						'typeid' => $typeid,
						'typename' => $typename,
						'status' => $status
					);
			$result = $this->cd->Insert($model);//进行插入
			return $result['insert_id']?$result['insert_id']:'';
		}
	}
